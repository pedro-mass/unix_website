
						<h2>File Manipulation</h2>
						<h3>Move (mv)</h3>

						<p>
							Similar to copy, to delete a file or directory you 
							use the same command:
							<br /><br />
							mv source destination
							<br /><br />
							This has a similar syntax to that of the copy 
							command. The difference is that instead of leaving 
							the original where it is, it moves to the new 
							destination. As a result, you end up with one file, 
							rather than two.
							<br /><br />
							Question: Using the move command, can you rename a 
							file?
							<br /><br />
							Answer: Yes! If your destination is within the same directory but has a different file name, it will move the file to the same directory and give it the new name.
							<br /><br />
							Ex: mv phone_numbers.txt phone_nums.txt
							This would rename the file.
							
