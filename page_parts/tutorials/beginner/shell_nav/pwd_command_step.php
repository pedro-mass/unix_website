						<h3 id="stage_subtitle">Present Working Directory (pwd)</h3>

						<p id="stage_description">
							Another useful command is pwd, short for present working directory. 
							Using this without anything after it will show you the absolute path
							to the directory you are currently in. It's great for figuring out 
							where out are.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>pwd</code>	
						</p>
						
						<p id="stage_steps">
							Use the above command in the terminal. It will display the absolute path to the 
							directory you are currently in.
						</p>
