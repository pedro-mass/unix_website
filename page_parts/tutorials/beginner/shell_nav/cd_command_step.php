<div id="stage_content">
						<h3 id="stage_subtitle">Change Directory (cd)</h3>

						<p id="stage_description">
							If you want to move to a different directory all you need to do is 
							use the "cd" command, cd being short for change directory. This command
							followed by a directory name will change your current directory to 
							whatever directory you specify.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>cd /home</code>	
						</p>
						
						<p id="stage_steps">
							Use the above command to change to the home directory, where you can see the other users. Also, if you use the cd
							command without any directories it will take you to your home directory.
						</p>
</div>