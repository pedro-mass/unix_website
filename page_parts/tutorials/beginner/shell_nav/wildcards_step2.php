<div id="stage_content">
						<h3 id="stage_subtitle">Wild Cards</h3>

						<p id="stage_description">
							Now we will try the wildcards. The question mark will take the place of any one character.
							The asterisk will take the place of any number of characters, and the square brackets will
							take the place of one of the listed characters.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>c?t c* [b,c]are</code>	
						</p>
						
						
						<p id="stage_steps">
							Try the three above commands separately, the first will search for any file that is 3 letters long, 
							starts with a "c" and ends with a "t". The second command will return any file that starts
							with a "c". And finally the last command will return any file that starts with either a "b"
							or "c" and ends with "are".
						</p>
</div>