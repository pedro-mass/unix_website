<div id="stage_content">
						<h3 id="stage_subtitle">List (li)</h3>

						<p id="stage_description">
							In order to see what other files and directories are around you, you can
							use the "ls" command. The command "ls", being short for list, will list all
							the files and directories that are in the same directory as the one you are
							currently in. 
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>li -la</code>	
						</p>
						
						<p id="stage_steps">
							Try the above command. This will list all the files in the same directory as you.
							You can see that we use two options. One is the -l option. This gives you more info
							and the -a option which shows all files, even hidden files.
						</p>
</div>