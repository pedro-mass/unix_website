
<h2>Help (man pages)</h2>

<p>
	Man pages are online manuals that give you all the information for a particular command.
</p>
<p>
	They man pages can tell you which options a specific command can take, and how each option changes the behavior of the command.
</p>
<p>
	The syntax is:
		man command
	Where command is the command you want help with.
</p>
<p>	
	All the information provided by the man pages can be overwhelming at times. If your system supports it, and you don’t care about the options a command can take, there is an alternative.
</p>
<p>
	For a one-line description of a command, without any options, you can use
		whatis command
</p>
<p>
	For example, “whatis cp” returns 2 lines for each use of cp. One describes copying files and the other for copying directories.
</p>
<p>
	Question: How do you interactively make sure that when copying a file, you don’t overwiret an existing file with the same name?
</p>
<p>
	Answer: Looking at the man pages, we see that we can use the -i option to do just this.
</p>
<p>
	Exercise: How do you copy a directory and all of its contents? (-r)
</p>

