						<h2>File Manipulation</h2>
						<h3>Copy (cp)</h3>

						<p>
							The command for copying files and directories are one and the same. that command 
							is:<br /><br />
								<strong>cp source destination</strong>
							<br /><br />
							Where source is the file or directory you want to copy, and destination...well 
							is the destination you want that copy to go to.
							<br /><br />
							Lets say that we had a file called phone_numbers.txt located at ~/docs, and you 
							are in your home directory (~).
							<br /><br />
							To make a copy of that file ("phone_numbers.txt") to your home directory, what would the command be?
							<br /><br />
							<strong>Answer:</strong>
							cp docs/phone_numbers.txt .
							<br /><br />
							The source is the location of the "phone_number.txt" file, and the "." (dot) as 
							our destination means using the current directory (which is currently home).
