<h2>File Name Conventions Summary:</h2>
<ul>	
	<li>Safest way to name a file
		<ul>
			<li>use alphanumeric characters (letters and numbers)</li>
			<li>can use _ (underscores) to simulate spacing</li> 
			<li>can use . (dot) to indicate the file extension</li>
		</ul>
	</li>
	<li>DONT	
		<ul>
			<li>use /, *, &, or %	As they are used for other things</li>
		</ul>
	</li>
	<li>Try not to have spaces in the file name</li>
	
