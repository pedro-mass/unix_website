<div id="stage_content">
						<h3 id="stage_subtitle">Visual Interface (vi)</h3>

						<p id="stage_description">
							In previous lessons we explained how to make an empty file with the touch command.
							Now if you want to add some content you will need to use an editing tool. There are 
							several ways to edit a file in Unix, but the one we will cover is vi. All you need
							to do to start editing is running the "vi newfile"
							command. This will create a new file named "newfile" and open the vi environment in your terminal.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>vi newfile</code>	
						</p>
						
						
						<p id="stage_steps">
							First, press the "insert" or "i' key, this will start the vi 
							editing mode. Next, add some content to the file. When finished, hit
							the "esc" key. Now type ":wq" and hit enter, dont forget the colon! 
							This will save the file and exit the vi interface.
						</p>
</div>