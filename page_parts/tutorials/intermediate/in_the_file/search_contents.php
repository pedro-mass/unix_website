
<h2>In the File: Searching Contents of File</h2>

<h3>less</h3>
<p>
	Less allows us to view the contents of the file, one page at a time, but it also provides a simple search facility. While viewing the file, you can type:
	<br /><br />
	/search_term
	<br /><br />
	and less finds and highlights the first occurrence of “search_term.” To find the next occurence, simply tap the n key.
	<br /><br />
	Another useful command to know, especially if you have a word count enforced on writing one of your papers:
</p>

<h3>wc (word count)</h3>
<p>
	This command has two main options to pay attention to.
	<br /><br />
	One for counting words<br />
	wc -w phone_numbers.txt
	<br /><br />
	The other for counting lines<br />
	wc -l phone_numbers.txt
</p>

