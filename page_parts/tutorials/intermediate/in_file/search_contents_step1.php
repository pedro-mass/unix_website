<div id="stage_content">
						<h3 id="stage_subtitle">The wc command</h3>

						<p id="stage_description">
							The wc or word count command allows you to count elements of a file. There are 2 main options
							used with wc. One for counting words: wc -w phone_numbers.txt. And one the other for counting
							lines: wc -l phone_numbers.txt.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>wc -w phone_numbers.txt</code>
						</p>
						
						<p id="stage_steps">
							If you had a file named "phone_numbers.txt", you would be able to see the number of words,
							or number of lines in that file.
						</p>
</div>