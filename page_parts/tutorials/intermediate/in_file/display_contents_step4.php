<div id="stage_content">
						<h3 id="stage_subtitle">The tail command</h3>

						<p id="stage_description">
							If you want to see the end of a file, the less command could help you.
							This command shows the last 10 lines of a file by default. This can be useful if 
							you want to look at the end of a log file to see what went wrong. 
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>tail filename</code>	
						</p>
						
						<p id="stage_steps">
							If you have a file named "filename" it will show you the last 10 lines of that
							file. Another common use for this command is with the -f option. The -f option
							will make the tail command a foreground process so you can see changes to the
							file live.
						</p>
</div>						
