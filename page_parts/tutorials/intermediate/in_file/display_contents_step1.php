<div id="stage_content">
						<h3 id="stage_subtitle">Clearing The Terminal</h3>

						<p id="stage_description">
							Clearing the screen removes all previous commands and results on the terminal screen
							and can allow you to focus on the new set of commands. It does not erase all previous
							commands, it just pushes the content up so the the only thing you see is the command
							prompt (can still scroll up to see previous commands).
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>clear</code>	
						</p>
						
						<p id="stage_steps">
							Try the above command. If successful, your screen should be cleared up to the terminal cursor.
						</p>
</div>