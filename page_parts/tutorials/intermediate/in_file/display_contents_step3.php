<div id="stage_content">
						<h3 id="stage_subtitle">The less command</h3>

						<p id="stage_description">
							Another command that allows you to see the contents of a file is the less command.
							This command writes out the content of the file, one page at a time. To go to the next
							page, tap the space bar. To exit out of viewing the contents, hit the q key.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>less filename</code>	
						</p>
						
						<p id="stage_steps">
							Try the above command. If you have a file named "filename" it should open the contents
							of the file and allow you to go page by page through the file.
						</p>
						
</div>