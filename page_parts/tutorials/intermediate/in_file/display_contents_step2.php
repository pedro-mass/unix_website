<div id="stage_content">
						<h3 id="stage_subtitle">Displaying Contents of File</h3>

						<p id="stage_description">
							The cat command displays the contents of the file on the screen all at once. Useful if the 
							length of the content is shorter than the terminal. Otherwise, you'll only
							catch the end of the file that fits on the screen.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>cat filename</code>	
						</p>
						
						<p id="stage_steps">
							Try the above command. If you have a file named "filename" it will display the contents
							of that file.
						</p>
</div>