<div id="stage_content">
						<h3 id="stage_subtitle">Find (find)</h3>

						<p id="stage_description">
							Another useful command for finding a certain file that you know the name of is find.
							Very intuitively named, the find command will find a file within a directory structure.
							be sure to replace the "your-name" in the command below with your login name.
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>find /home/your-name filename</code>	
						</p>
						
						<p id="stage_steps">
							The above command will search for a file named "cat" within the home directory. 
							You can substitute the starting directory (where we used /home/your-name) with any directory you
							want to search in.
						</p>
</div>