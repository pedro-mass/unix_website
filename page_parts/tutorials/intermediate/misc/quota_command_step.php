<div id="stage_content">
						<h3 id="stage_subtitle">Quota (quota)</h3>

						<p id="stage_description">
							After making a few files and saving them you may want to know about
							the disk usage in your system. This is accomplished through the quota
							command. By simply using the "quota" command you can see the disk 
							usage and limits. By default this only shows the disk usage for the
							current user, you!
						</p>
						
						<p id="stage_example">
							<div id="code_example">
								Code example: 
							</div>
								<code>quota</code>	
						</p>
						
						<p id="stage_steps">
							This command will not work in our online terminal as it does not have a disk
							to show stats for. This command however, will work in any other UNIX based system.
						</p>
</div>