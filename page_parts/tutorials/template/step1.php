						<h3>Absolute Paths</h3>

						<p>
							In addition to relative paths there are also absolute paths. These 
							paths always start with a slash (the root directory) and you have to specify the entire path
							to the directory you want to go to.   
						</p>
						
						<p>
							For example, if I wanted to go to the projects directory like in the previous lesson,
							but you don't know the relative path from where you are, you can simply use the 
							following command:
						</p>
						
						<p>
							 cd /home/documents/projects
						</p>
						
						<p>
							The advantage to the above command and absolute type paths in general, is that absolute paths will work
							anywhere you are in the UNIX file system since. This is useful if you don't know the relative path to a
							directory you want to go to.
						</p>
